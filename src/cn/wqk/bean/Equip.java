package cn.wqk.bean;

public class Equip {
	String equip_id,equip_name,equip_shuxing,equip_price,equip_describe,equip_history,equip_type,equip_number;

	public String getEquip_number() {
		return equip_number;
	}
	public void setEquip_number(String equip_number) {
		this.equip_number = equip_number;
	}
	public String getEquip_id() {
		return equip_id;
	}
	public void setEquip_id(String equipId) {
		equip_id = equipId;
	}
	public String getEquip_name() {
		return equip_name;
	}
	public void setEquip_name(String equipName) {
		equip_name = equipName;
	}
	public String getEquip_shuxing() {
		return equip_shuxing;
	}
	public void setEquip_shuxing(String equipShuxing) {
		equip_shuxing = equipShuxing;
	}
	public String getEquip_price() {
		return equip_price;
	}
	public void setEquip_price(String equipPrice) {
		equip_price = equipPrice;
	}
	public String getEquip_describe() {
		return equip_describe;
	}
	public void setEquip_describe(String equipDescribe) {
		equip_describe = equipDescribe;
	}
	public String getEquip_history() {
		return equip_history;
	}
	public void setEquip_history(String equipHistory) {
		equip_history = equipHistory;
	}
	public String getEquip_type() {
		return equip_type;
	}
	public void setEquip_type(String equipType) {
		equip_type = equipType;
	}
}
